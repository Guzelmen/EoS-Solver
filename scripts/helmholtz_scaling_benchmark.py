"""
GPU scaling benchmark — total time vs number of conditions.

Tests five batch sizes by running the full batched Helmholtz pipeline
at each condition count:

    100   (10×10)
    400   (20×20)
    1024  (32×32)
    10000 (100×100)
    40000 (200×200)

For each size the ρ and T grids are generated by drawing the required
number of log-spaced values within the min/max of the reference lists
(no extrapolation).

Timing follows the same protocol as helmholtz_batch_benchmark.py:
  - N_WARMUP discarded runs
  - N_TIMED measured runs
  - Report mean ± std of t_forward, t_postproc, t_total
  - And the per-condition cost

Results are printed as a table and also saved to a numpy .npz file
alongside this script for plotting.

Usage
-----
    python -m scripts.helmholtz_scaling_benchmark \\
        --config configs/helmholtz_pipeline.yaml --device cuda
"""

import argparse
import math
import sys
import time
from pathlib import Path

import matplotlib
matplotlib.use("Agg")   # non-interactive backend — safe on HPC nodes
import matplotlib.pyplot as plt
import numpy as np
import torch

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from src.model_loader import load_eos_config, load_pinn, build_x_grid
from src.inputs import (
    z_scale_inputs,
    compute_gamma,
    compute_lambda,
    r0_from_density,
    B_M,
    KEV_TO_J,
    M_PROTON,
)
from src.fd_integrals import fermi_dirac_half, fermi_dirac_three_half
from src.quantities.internal_energy import C_K, C_UEN, C_PRESSURE

# ---------------------------------------------------------------------------
# Fixed element
# ---------------------------------------------------------------------------

Z = 26.0   # Iron
A = 56.0

# ---------------------------------------------------------------------------
# Reference ranges (taken from the 10-point lists used in the 100-cond run)
# New grids are drawn log-spaced within these bounds — no extrapolation.
# ---------------------------------------------------------------------------

RHO_MIN, RHO_MAX = 0.2996, 54.62    # g/cm³
T_MIN,   T_MAX   = 0.1476, 14.660   # keV

# Batch sizes to sweep: (n_rho, n_T) -> n_conditions = n_rho * n_T
BATCH_SIZES = [
    (10,  10),    #    100
    (20,  20),    #    400
    (32,  32),    #   1024
    (64,  64),    #   1024
    (100, 100),   #  10000
    (120, 120),   #  10000
    (130, 130),   #  10000
    (140, 140),   #  10000
]

# ---------------------------------------------------------------------------
# Benchmark parameters
# ---------------------------------------------------------------------------

N_WARMUP = 5
N_TIMED  = 20

# ---------------------------------------------------------------------------
# Timing helpers
# ---------------------------------------------------------------------------

def _sync(device: str):
    if device != "cpu" and torch.cuda.is_available():
        torch.cuda.synchronize()

def _tick(device: str) -> float:
    _sync(device)
    return time.perf_counter()

def _tock(t0: float, device: str) -> float:
    _sync(device)
    return (time.perf_counter() - t0) * 1e3   # ms

# ---------------------------------------------------------------------------
# Condition parameter preparation
# ---------------------------------------------------------------------------

def _make_grids(n_rho: int, n_T: int):
    """Log-spaced ρ and T grids within reference bounds."""
    rho_grid = np.logspace(np.log10(RHO_MIN), np.log10(RHO_MAX), n_rho)
    T_grid   = np.logspace(np.log10(T_MIN),   np.log10(T_MAX),   n_T)
    return rho_grid, T_grid


def _build_condition_params(rho_list, T_list):
    """Compute (alpha_1, T_1, gamma, lam, r_0_1, V_1) for the full ρ×T grid."""
    conditions = []
    for rho in rho_list:
        for T_keV in T_list:
            r0           = r0_from_density(rho, A)
            alpha_1, T_1 = z_scale_inputs(Z, r0, T_keV)
            gamma        = compute_gamma(T_1)
            lam          = compute_lambda(alpha_1, T_1)
            r_0_1        = alpha_1 * B_M
            V_1          = (4.0 / 3.0) * math.pi * r_0_1**3
            conditions.append((alpha_1, T_1, gamma, lam, r_0_1, V_1))

    def _col(i, dt=np.float32):
        return np.array([c[i] for c in conditions], dtype=dt)

    return _col(0), _col(1), _col(2), _col(3), _col(4, np.float64), _col(5, np.float64)

# ---------------------------------------------------------------------------
# Single timed pass
# ---------------------------------------------------------------------------

def _run_once(model, inputs_base_t, x_t, gamma_t, lam_t, r_0_1_t, V_1_t, T_1_t,
              device, n_cond, n_x):
    # Detach from any previous graph and create a new leaf tensor — shares the
    # same GPU memory as inputs_base_t so there is no copy or PCIe transfer.
    inputs_t = inputs_base_t.detach().requires_grad_(True)

    # Forward
    t0_fwd = _tick(device)
    phi_flat = model(inputs_t)
    phi_t    = phi_flat.reshape(n_cond, n_x).detach()
    del inputs_t   # release graph immediately; inputs_base_t storage is unaffected
    t_forward = _tock(t0_fwd, device)

    # Post-processing
    t0_post = _tick(device)

    xi_t     = gamma_t.unsqueeze(1) * phi_t / (lam_t.unsqueeze(1) * x_t.unsqueeze(0))
    xi_1_t   = xi_t[:, -1]

    xi_col_t = xi_t.unsqueeze(2)
    F32_t    = fermi_dirac_three_half(xi_col_t).squeeze(2)
    F12_t    = fermi_dirac_half(xi_col_t).squeeze(2)

    xi_1_col_t = xi_1_t.unsqueeze(1).unsqueeze(2)
    F32_b_t    = fermi_dirac_three_half(xi_1_col_t).squeeze(2).squeeze(1)

    trap_K   = torch.trapezoid(F32_t * x_t.unsqueeze(0)**2, x_t, dim=1)
    trap_Uen = torch.trapezoid(F12_t * x_t.unsqueeze(0),    x_t, dim=1)

    K_1_t    = C_K   * r_0_1_t**3 * T_1_t**2.5 * trap_K
    U_en_1_t = -C_UEN * r_0_1_t**2 * T_1_t**1.5 * trap_Uen

    P_e1_t   = C_PRESSURE * T_1_t**2.5 * F32_b_t
    U_ee_1_t = 3.0 * P_e1_t * V_1_t - 2.0 * K_1_t - U_en_1_t

    mu_e1_t  = xi_1_t * T_1_t * KEV_TO_J
    F_Z1_t   = mu_e1_t - (2.0 / 3.0) * K_1_t - U_ee_1_t
    _        = Z**(7.0 / 3.0) * F_Z1_t   # F_phys_t — computed to ensure no shortcuts

    t_postproc = _tock(t0_post, device)

    return t_forward, t_postproc

# ---------------------------------------------------------------------------
# Benchmark one batch size
# ---------------------------------------------------------------------------

def benchmark_size(model, n_rho, n_T, cfg, device):
    n_x   = cfg.get("n_x",   256)
    x_min = cfg.get("x_min", 1e-6)
    n_cond = n_rho * n_T

    rho_grid, T_grid = _make_grids(n_rho, n_T)
    alpha_1_arr, T_1_arr, gamma_arr, lam_arr, r_0_1_arr, V_1_arr = (
        _build_condition_params(rho_grid, T_grid)
    )

    # Upload input tensor to GPU once — _run_once does a zero-copy .detach()
    # each call to get a fresh leaf tensor without re-transferring data.
    x_np      = build_x_grid(n_x, x_min)
    x_rep     = np.tile(x_np.astype(np.float32), n_cond)
    alpha_rep = np.repeat(alpha_1_arr, n_x)
    T_rep     = np.repeat(T_1_arr,     n_x)
    inputs_np = np.stack([x_rep, alpha_rep, T_rep], axis=1)
    inputs_base_t = torch.from_numpy(inputs_np).to(device)   # uploaded once, no grad

    def _t(arr):
        return torch.from_numpy(arr.astype(np.float32)).to(device)

    x_t     = torch.from_numpy(x_np.astype(np.float32)).to(device)
    gamma_t  = _t(gamma_arr)
    lam_t    = _t(lam_arr)
    r_0_1_t  = _t(r_0_1_arr)
    V_1_t    = _t(V_1_arr)
    T_1_t    = _t(T_1_arr)

    # Warmup
    for _ in range(N_WARMUP):
        _run_once(model, inputs_base_t, x_t, gamma_t, lam_t, r_0_1_t, V_1_t, T_1_t,
                  device, n_cond, n_x)

    # Timed runs
    fwd_times  = np.empty(N_TIMED)
    post_times = np.empty(N_TIMED)
    for i in range(N_TIMED):
        tf, tp = _run_once(model, inputs_base_t, x_t, gamma_t, lam_t, r_0_1_t, V_1_t, T_1_t,
                           device, n_cond, n_x)
        fwd_times[i]  = tf
        post_times[i] = tp

    total_times = fwd_times + post_times
    return {
        "n_cond":        n_cond,
        "fwd_mean":      fwd_times.mean(),
        "fwd_std":       fwd_times.std(),
        "post_mean":     post_times.mean(),
        "post_std":      post_times.std(),
        "total_mean":    total_times.mean(),
        "total_std":     total_times.std(),
        "fwd_pc_mean":   fwd_times.mean()   / n_cond,
        "fwd_pc_std":    fwd_times.std()    / n_cond,
        "post_pc_mean":  post_times.mean()  / n_cond,
        "post_pc_std":   post_times.std()   / n_cond,
        "total_pc_mean": total_times.mean() / n_cond,
        "total_pc_std":  total_times.std()  / n_cond,
    }

# ---------------------------------------------------------------------------
# Plotting
# ---------------------------------------------------------------------------

def _plot_scaling(arrays, plot_dir: Path, device: str):
    n   = arrays["n_cond"]
    col = {"total": "#2176ae", "fwd": "#e07b39", "post": "#57a773"}

    # ---- Figure 1: total wall time vs N_conditions -------------------------
    fig, ax = plt.subplots(figsize=(7, 5))
    ax.errorbar(n, arrays["total_mean"], yerr=arrays["total_std"],
                fmt="o-", color=col["total"], label="Total", capsize=4, lw=2)
    ax.errorbar(n, arrays["fwd_mean"],   yerr=arrays["fwd_std"],
                fmt="s--", color=col["fwd"],   label="Forward pass", capsize=4, lw=1.5)
    ax.errorbar(n, arrays["post_mean"],  yerr=arrays["post_std"],
                fmt="^--", color=col["post"],  label="Post-processing", capsize=4, lw=1.5)
    ax.set_xscale("log")
    ax.set_yscale("log")
    ax.set_xlabel("Number of conditions", fontsize=12)
    ax.set_ylabel("Wall time (ms)", fontsize=12)
    ax.set_title(f"Batched Helmholtz — scaling  [{device}]", fontsize=13)
    ax.legend(fontsize=10)
    ax.grid(True, which="both", ls=":", alpha=0.5)
    fig.tight_layout()
    p = plot_dir / "scaling_total_time.pdf"
    fig.savefig(p, dpi=150)
    fig.savefig(p.with_suffix(".png"), dpi=150)
    plt.close(fig)
    print(f"Plot saved to {p}")

    # ---- Figure 2: per-condition cost vs N_conditions ----------------------
    pc_mean = arrays["total_pc_mean"] * 1e3   # µs
    pc_std  = arrays["total_pc_std"]  * 1e3

    fig, ax = plt.subplots(figsize=(7, 5))
    ax.errorbar(n, pc_mean, yerr=pc_std,
                fmt="o-", color=col["total"], capsize=4, lw=2)
    ax.set_xscale("log")
    ax.set_xlabel("Number of conditions", fontsize=12)
    ax.set_ylabel("Time per condition (µs)", fontsize=12)
    ax.set_title(f"Per-condition cost vs batch size  [{device}]", fontsize=13)
    ax.grid(True, which="both", ls=":", alpha=0.5)
    fig.tight_layout()
    p = plot_dir / "scaling_per_condition.pdf"
    fig.savefig(p, dpi=150)
    fig.savefig(p.with_suffix(".png"), dpi=150)
    plt.close(fig)
    print(f"Plot saved to {p}")

    # ---- Figure 3: stacked bar — fwd vs post breakdown ---------------------
    fig, ax = plt.subplots(figsize=(7, 5))
    bar_w = 0.5
    x_pos = np.arange(len(n))
    ax.bar(x_pos, arrays["fwd_mean"],  bar_w, label="Forward pass",    color=col["fwd"])
    ax.bar(x_pos, arrays["post_mean"], bar_w, bottom=arrays["fwd_mean"],
           label="Post-processing", color=col["post"])
    ax.set_xticks(x_pos)
    ax.set_xticklabels([str(int(v)) for v in n], rotation=30, ha="right")
    ax.set_xlabel("Number of conditions", fontsize=12)
    ax.set_ylabel("Wall time (ms)", fontsize=12)
    ax.set_title(f"Forward vs post-processing breakdown  [{device}]", fontsize=13)
    ax.legend(fontsize=10)
    ax.grid(True, axis="y", ls=":", alpha=0.5)
    fig.tight_layout()
    p = plot_dir / "scaling_breakdown.pdf"
    fig.savefig(p, dpi=150)
    fig.savefig(p.with_suffix(".png"), dpi=150)
    plt.close(fig)
    print(f"Plot saved to {p}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def run_scaling_benchmark(cfg, device):
    print(f"\n{'='*70}")
    print(f"GPU scaling benchmark  —  Iron (Z=26, A=56)")
    print(f"  ρ range : [{RHO_MIN}, {RHO_MAX}] g/cm³  (log-spaced)")
    print(f"  T range : [{T_MIN}, {T_MAX}] keV  (log-spaced)")
    print(f"  Device  : {device}")
    print(f"  Warmup  : {N_WARMUP}  |  Timed : {N_TIMED}")
    print(f"{'='*70}\n")

    model, _ = load_pinn(cfg, device=device, verbose=False)
    model.eval()
    torch.set_grad_enabled(True)

    results = []
    for n_rho, n_T in BATCH_SIZES:
        n_cond = n_rho * n_T
        print(f"--- {n_rho}×{n_T} = {n_cond:>6} conditions  "
              f"(input tensor: [{n_cond * cfg.get('n_x', 256)}, 3]) ---")
        r = benchmark_size(model, n_rho, n_T, cfg, device)
        torch.cuda.empty_cache()   # release any cached allocations before next size
        results.append(r)
        print(f"    total  : {r['total_mean']:8.2f} ± {r['total_std']:.2f} ms  "
              f"| per-cond: {r['total_pc_mean']*1e3:.4f} ± {r['total_pc_std']*1e3:.4f} µs")
        print(f"    fwd    : {r['fwd_mean']:8.2f} ± {r['fwd_std']:.2f} ms  "
              f"| per-cond: {r['fwd_pc_mean']*1e3:.4f} ± {r['fwd_pc_std']*1e3:.4f} µs")
        print(f"    post   : {r['post_mean']:8.2f} ± {r['post_std']:.2f} ms  "
              f"| per-cond: {r['post_pc_mean']*1e3:.4f} ± {r['post_pc_std']*1e3:.4f} µs")
        print()

    # ------------------------------------------------------------------
    # Summary table
    # ------------------------------------------------------------------
    cw = 10
    print(f"\n{'='*70}")
    print(f"Summary table")
    print(f"{'='*70}")
    header = (f"  {'N_cond':>{cw}}  {'Total (ms)':>14}  {'Fwd (ms)':>14}  "
              f"{'Post (ms)':>14}  {'µs/cond':>10}")
    print(header)
    print("  " + "\u2500" * (len(header) - 2))
    for r in results:
        print(f"  {r['n_cond']:>{cw}}  "
              f"{r['total_mean']:>8.2f}±{r['total_std']:<5.2f}  "
              f"{r['fwd_mean']:>8.2f}±{r['fwd_std']:<5.2f}  "
              f"{r['post_mean']:>8.2f}±{r['post_std']:<5.2f}  "
              f"{r['total_pc_mean']*1e3:>10.4f}")
    print()

    # ------------------------------------------------------------------
    # Save results
    # ------------------------------------------------------------------
    plot_dir = Path(__file__).resolve().parents[1] / "plots" / "helmholtz_scaling_extended"
    plot_dir.mkdir(parents=True, exist_ok=True)

    npz_path = plot_dir / "helmholtz_scaling_results.npz"
    arrays = {
        "n_cond":        np.array([r["n_cond"]        for r in results]),
        "total_mean":    np.array([r["total_mean"]     for r in results]),
        "total_std":     np.array([r["total_std"]      for r in results]),
        "fwd_mean":      np.array([r["fwd_mean"]       for r in results]),
        "fwd_std":       np.array([r["fwd_std"]        for r in results]),
        "post_mean":     np.array([r["post_mean"]      for r in results]),
        "post_std":      np.array([r["post_std"]       for r in results]),
        "total_pc_mean": np.array([r["total_pc_mean"]  for r in results]),
        "total_pc_std":  np.array([r["total_pc_std"]   for r in results]),
    }
    np.savez(npz_path, **arrays)
    print(f"Results saved to {npz_path}")

    # ------------------------------------------------------------------
    # Plot
    # ------------------------------------------------------------------
    _plot_scaling(arrays, plot_dir, device)


def parse_args():
    parser = argparse.ArgumentParser(
        description="GPU scaling benchmark: total time vs number of conditions."
    )
    parser.add_argument("--config", type=str, default=None)
    parser.add_argument("--device", type=str, default=None,
                        choices=["cpu", "cuda", "auto"])
    return parser.parse_args()


def main():
    args   = parse_args()
    cfg    = load_eos_config(args.config)
    device = args.device or "cuda"
    if device == "auto":
        device = "cuda" if torch.cuda.is_available() else "cpu"
    run_scaling_benchmark(cfg, device)


if __name__ == "__main__":
    main()
